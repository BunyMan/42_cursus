#include "libft.h"

int	ft_atoi(const char *str)
{
	unsigned int	i;
	unsigned int	res;
	int				sign;

	i = 0;
	res = 0;
	sign = 1;
	if (!*str)
		return (0);
	while (str[i] == ' ' || str[i] == '\v' || str[i] == '\n' || str[i] == '\t'
		|| str[i] == '\r' || str[i] == '\f')
		i++;
	if (str[i] == '-')
	{
		sign = -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = (res * 10) + (str[i] - '0');
		i++;
	}
	return (res * sign);
}
